#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (*(char *) s == '\0' + 1)
	{
		return (NULL);
	}
	else if (*(char *) s != c)
	{
		s++;
		return (ft_strchr(s, c));
	}
	return ((char *) s);
}
