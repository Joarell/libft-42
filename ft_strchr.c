#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (*s == '\0' && c != '\0')
	{
		return (NULL);
	}
	if (*s != c)
	{
		s++;
		return (ft_strchr(s, c));
	}
	return ((char *) s);
}
