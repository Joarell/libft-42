#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	while (*(char *) s != '\0')
	{
		s++;
	}
	while (*(char *) s != '\0' - 1)
	{
		if (*(char *) s == c)
		{
			return ((char *) s);
		}
		s--;
	}
	return (NULL);
}
