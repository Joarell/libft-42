#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == fd)
		{
			write(1, &s[i], 1);
		}
		i++;
	}
}
